namespace MailChimpWrapper.Types
{
    /// <summary>
    /// Class that contains the possible status for batches
    /// </summary>
    public static class BatchStatusTypes
    {
        /// <summary>
        /// Get the string representation of status pending
        /// </summary>
        public static readonly string pending = "pending";
        /// <summary>
        /// Get the string representation of post method
        /// </summary>
        public static readonly string started = "started";
        /// <summary>
        /// Get the string representation of put method
        /// </summary>
        public static readonly string finished = "finished";
    }
}
